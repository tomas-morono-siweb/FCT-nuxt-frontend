<script setup lang="ts">
import type { Coach } from "~/interfaces/coach";
import type { Club } from "~/interfaces/club";
import type { BackendError } from "~/interfaces/validation";

const route = useRoute();
const id = Number(route.params.id);
const { get, update } = useCoaches();
const { list: listClubs } = useClubs();

// Form errors handling
const formErrors = useFormErrors();
const { setErrors, clearErrors, clearFieldError, getFieldError, hasErrors, generalError } = formErrors;

// Provide form errors to child components
provide("formErrors", formErrors);

// Load coach data
const { data: coach, pending, error } = await useAsyncData<Coach>(`coach:${id}`, () => get(id));

// Form data - initialize with coach data
const form = reactive<Partial<Coach>>({
  dni: "",
  nombre: "",
  apellidos: "",
  salario: undefined,
  id_club: "", // Cambiar undefined por cadena vac√≠a
});

// Watch for coach data changes to populate form
watch(
  coach,
  (newCoach) => {
    if (newCoach) {
      form.dni = newCoach.dni;
      form.nombre = newCoach.nombre;
      form.apellidos = newCoach.apellidos;
      form.salario = newCoach.salario;
      form.id_club = newCoach.id_club || ""; // Usar cadena vac√≠a si no hay club
    }
  },
  { immediate: true }
);

// Loading states
const loading = ref(false);

// Load clubs for selection - OPTIMIZADO: usar cach√© compartido
const { data: clubsResponse } = await useAsyncData("clubs-list", () => listClubs(), {
  default: () => ({ data: [], pagination: null }),
  server: false, // Solo cargar en cliente para evitar SSR duplicado
});
const clubs = computed(() => clubsResponse.value?.data || []);

// Computed para manejar la selecci√≥n del club
const selectedClubId = computed({
  get: () => {
    if (form.id_club) {
      const club = clubs.value.find((c) => c.id_club === form.id_club);
      return club ? club.nombre : "";
    }
    return "";
  },
  set: (clubName: string) => {
    const club = clubs.value.find((c) => c.nombre === clubName);
    form.id_club = club ? club.id_club : ""; // Cambiar undefined por cadena vac√≠a
  },
});

// Submit handler
const handleSubmit = async () => {
  loading.value = true;
  clearErrors();

  console.log("üìù Datos del formulario antes de enviar:", JSON.stringify(form, null, 2));

  try {
    await update(id, form);

    console.log("‚úÖ Entrenador actualizado exitosamente");

    // Invalidar el cache de datos para forzar la recarga
    await clearNuxtData(`coach:${id}`);

    // Invalidar cache del club si cambi√≥
    if (form.id_club) {
      const selectedClub = clubs.value.find((c) => c.id_club === form.id_club);
      if (selectedClub) {
        await clearNuxtData(`club:${selectedClub.id}`);
      }
    }

    await navigateTo(`/coaches/${id}`);
  } catch (err: any) {
    console.log("‚ùå Error al actualizar entrenador:", err);
    setErrors(err);
  } finally {
    loading.value = false;
  }
};
</script>

<template>
  <div class="min-h-screen bg-gray-50 px-4 py-6 sm:px-6 lg:px-8">
    <div class="mx-auto max-w-3xl">
      <!-- Breadcrumb -->
      <UiBreadcrumb
        color="green"
        class="mb-4"
        :items="[
          { label: 'Inicio', to: '/', icon: 'home' },
          { label: 'Entrenadores', to: '/coaches', icon: 'users' },
          { label: 'Editar', to: '', icon: 'edit' },
          { label: coach ? `${coach.nombre} ${coach.apellidos}` : 'Entrenador', to: '', icon: 'user' },
        ]"
      />

      <!-- Header Section -->
      <UiPageHeader
        title="Editar Entrenador"
        :description="
          coach ? `Editando informaci√≥n de ${coach.nombre} ${coach.apellidos}` : 'Editando informaci√≥n del entrenador'
        "
        back-to="/coaches"
      />

      <!-- Loading State -->
      <UiLoadingState
        v-if="pending"
        message="Cargando informaci√≥n del entrenador..."
        color="green"
      />

      <!-- Error State -->
      <UiErrorState
        v-else-if="error"
        :message="error.message"
      />

      <!-- Form Section -->
      <div
        v-else-if="coach"
        class="mt-8"
      >
        <div class="rounded-lg bg-white shadow-sm">
          <div class="border-b border-gray-200 px-6 py-4">
            <h3 class="text-lg font-medium text-gray-900">Informaci√≥n del Entrenador</h3>
            <p class="mt-1 text-sm text-gray-500">Modifica los datos del entrenador</p>
          </div>

          <form
            @submit.prevent="handleSubmit"
            class="space-y-6 p-6"
          >
            <!-- General Error Message (solo si hay error general) -->
            <div
              v-if="generalError"
              class="rounded-lg bg-red-50 p-4"
            >
              <div class="flex">
                <div class="flex-shrink-0">
                  <svg
                    class="h-5 w-5 text-red-400"
                    fill="currentColor"
                    viewBox="0 0 20 20"
                  >
                    <path
                      fill-rule="evenodd"
                      d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z"
                      clip-rule="evenodd"
                    />
                  </svg>
                </div>
                <div class="ml-3">
                  <h3 class="text-sm font-medium text-red-800">Error</h3>
                  <div class="mt-2 text-sm text-red-700">
                    {{ generalError }}
                  </div>
                </div>
              </div>
            </div>

            <!-- Form Fields -->
            <div class="grid gap-6 sm:grid-cols-2">
              <!-- DNI -->
              <UiFormField
                v-model="form.dni"
                label="DNI"
                placeholder="DNI del entrenador"
                required
                field-name="dni"
                @focus="clearFieldError"
              />

              <!-- Nombre -->
              <UiFormField
                v-model="form.nombre"
                label="Nombre"
                placeholder="Nombre del entrenador"
                required
                field-name="nombre"
                @focus="clearFieldError"
              />

              <!-- Apellidos -->
              <UiFormField
                v-model="form.apellidos"
                label="Apellidos"
                placeholder="Apellidos del entrenador"
                required
                field-name="apellidos"
                @focus="clearFieldError"
              />

              <!-- Salario -->
              <UiFormField
                v-model="form.salario"
                label="Salario Anual (‚Ç¨)"
                placeholder="Ej: 1500000"
                type="number"
                field-name="salario"
                @focus="clearFieldError"
              />

              <!-- Club -->
              <UiFormField
                v-model="selectedClubId"
                label="Club"
                :options="[
                  { value: '', label: 'Sin club' },
                  ...(clubs?.map((club) => ({ value: club.nombre, label: club.nombre })) || []),
                ]"
                field-name="id_club"
                @focus="clearFieldError"
              />
            </div>

            <!-- Form Actions -->
            <UiFormActions
              :loading="loading"
              submit-text="Guardar Cambios"
              cancel-text="Cancelar"
              :cancel-to="`/coaches/${id}`"
              @submit="handleSubmit"
            />
          </form>
        </div>
      </div>
    </div>
  </div>
</template>
